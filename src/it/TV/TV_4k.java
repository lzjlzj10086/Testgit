package it.TV;

/**
 * 4k超清电视
 */
public class TV_4k {
    /**
     * 屏幕大小
     */
    int size;
    /**
     * 电视品牌
     */
    String Brand;
    /**
     * 价格
     */
    double price;


    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public String getBrand() {
        return Brand;
    }

    public void setBrand(String brand) {
        Brand = brand;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
}
